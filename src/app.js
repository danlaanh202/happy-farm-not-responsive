const gameActivitiesItems = document.querySelector(".game-activities-items");

let initialIndexOfGameActivities = 3;
let limit = 3;
function addSlide() {
  gameActivitiesItems.innerHTML = gameActivitiesList
    .map((item, index) => {
      if (
        index <= initialIndexOfGameActivities &&
        index >= initialIndexOfGameActivities - limit
      ) {
        return `
         <div class="game-activities-item">
               <img
               src="${item.img}"
               alt=""
               class="game-activities-img"
               />
               <h3 class="game-activities-title">${item.name}</h3>
               <p class="game-activities-description">
               ${item.description}
               </p>
            </div>
         `;
      }
    })
    .join("");
}
addSlide();

function disappearElement(elementArray) {
  elementArray.forEach((item, index) => {
    item.style.animation = "disappear 0.5s linear";
  });
}

gameActivitiesNextBtn.addEventListener("click", () => {
  if (initialIndexOfGameActivities > gameActivitiesList.length) {
    return;
  }
  const oldElement = document.querySelectorAll(".game-activities-item");
  disappearElement(oldElement);
  initialIndexOfGameActivities += 4;
  setTimeout(addSlide, 500);
});
gameActivitiesPrevBtn.addEventListener("click", () => {
  if (initialIndexOfGameActivities <= 3) {
    initialIndexOfGameActivities = 3;
    return;
  }
  const oldElement = document.querySelectorAll(".game-activities-item");
  disappearElement(oldElement);
  initialIndexOfGameActivities -= 4;
  setTimeout(addSlide, 500);
});

